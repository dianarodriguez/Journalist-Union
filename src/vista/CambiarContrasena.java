    private void jButton1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MousePressed
        // TODO add your handling code here:
            if ((jTextField1.getText().isEmpty()) || (jPasswordField1.getText().isEmpty()) || (jPasswordField2.getText().isEmpty())
                    || (jPasswordField3.getText().isEmpty()))
                {
            JOptionPane.showMessageDialog(null, "Debe introducir todos los datos correctamnte.");
        } else {
            try {
                UsuarioControl gestion = new UsuarioControl();
                String contrasenav = "";
                contrasenav = EncriptarClave.encriptarMD5(jPasswordField1.getText());
                boolean existe = gestion.verificarUsuario(jTextField1.getText(), contrasenav);
                if (existe == true) {
                    if (jPasswordField2.getText().compareTo(jPasswordField3.getText()) == 0) {
                        String contrasena = "";
                        contrasena = EncriptarClave.encriptarMD5(jPasswordField2.getText());
                        try {
                            gestion.modificarPass(jTextField1.getText(), contrasena);
                        } catch (PreexistingEntityException ex) {
                            Logger.getLogger(CambiarContrasena.class.getName()).log(Level.SEVERE, null, ex);
                        } catch (Exception ex) {
                            Logger.getLogger(CambiarContrasena.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        JOptionPane.showMessageDialog(null, "Los contrase単a ha sido cambiada correctamente.");
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(null, "La nueva contrase単a y su confirmaci坦n son diferentes.");
                        jPasswordField2.setText("");
                        jPasswordField3.setText("");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "No existe un usuarios con esa cuenta o contrase単a, introduzaca los datos correctamnte.");
                    jTextField1.setText("");
                    jPasswordField1.setText("");
                    jPasswordField2.setText("");
                    jPasswordField3.setText("");
                }
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(CambiarContrasena.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButton1MousePressed

