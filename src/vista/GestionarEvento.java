                        e.setLugarEvento(lugar);
                        e.setNombreEvento(nombre);
                        e.setPaisEvento(pais);
                        e.setFechaEvento(jDateChooser1.getDate());
                        ec.adicionar(e);
                        jTextField1.setText("");
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jDateChooser1.setDate(Calendar.getInstance().getTime());
                        eventoList.add(e);
