    private void jButton1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MousePressed
        // TODO add your handling code here:
           if (jTable1.getSelectedRow()!= -1){
             jTable1.clearSelection();
             JOptionPane.showMessageDialog(null, "Para insertar no debe tener ningún premio seleccionado.");
        jTextField1.setText("");
             jTextArea1.setText("");
         }
        else{
             if(jTextField1.getText().isEmpty() ||jTextArea1.getText().isEmpty()){
              JOptionPane.showMessageDialog(null, "No debe dejar campos vacíos.");
             }
 else{
               PremioControl pc=new PremioControl();
                 if(pc.existe(jTextField1.getText())){
                      JOptionPane.showMessageDialog(null, "ya existe.");
                 jTextField1.setText("");

                  jTextArea1.setText("");
                 }
                 else {
                    try {
                        String nombre = jTextField1.getText();
                        String descripcion = jTextArea1.getText();
                        Premio p = new Premio();
                        p.setDescripcionPremio(descripcion);
                        p.setNombrePremio(nombre);
                        pc.adicionar(p);
                        jTextField1.setText("");
                        jTextArea1.setText("");
                        premioList.add(p);
                    } catch (PreexistingEntityException ex) {
                        Logger.getLogger(GestionarPremio.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (Exception ex) {
                        Logger.getLogger(GestionarPremio.class.getName()).log(Level.SEVERE, null, ex);
                    }





                 }
 }
        }
    }//GEN-LAST:event_jButton1MousePressed

