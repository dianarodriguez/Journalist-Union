    private void jButton3MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MousePressed
        // TODO add your handling code here:
         if (jTable1.getSelectedRow()== -1){
                 JOptionPane.showMessageDialog(null, "Para eliminar debe seleccionar el premio.");
               }
 else{
              Integer fila = jTable1.getSelectedRow();
              if(jTable1.getValueAt(fila, 0)==null || jTable1.getSelectedRowCount()>1)
                  JOptionPane.showMessageDialog(null, "Debe seleccionar un  premio.");
              else{
              if (JOptionPane.showConfirmDialog(null, "Realmente desea eliminar?") == JOptionPane.YES_OPTION) {

                   // Integer fila = jTable1.getSelectedRow();
               PremioControl pc=new PremioControl();
                    String nombre = String.valueOf(jTable1.getValueAt(fila, 0));
 int id = pc.buscarIdPremio(String.valueOf(jTable1.getValueAt(fila, 0)));
                Premio p=new Premio();
                PeriodistaPremioControl ppc=new PeriodistaPremioControl();
                p=pc.buscarPremio(nombre);
                    if (ppc.premioAsigando(id)) {

                        JOptionPane.showMessageDialog(null, "No se puede mientras esté asignado a un periodista.");
                    } else {
                        try {
                            pc.eliminar(id);
                            jTextField1.setText("");
                            jTextArea1.setText("");
                            premioList.remove(p);
                        } catch (NonexistentEntityException ex) {
                            Logger.getLogger(GestionarPremio.class.getName()).log(Level.SEVERE, null, ex);
                        }


                    }
                  }
                  }

        }
    }//GEN-LAST:event_jButton3MousePressed

