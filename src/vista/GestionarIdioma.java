    private void jButton2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MousePressed
        // TODO add your handling code here:
         if (jTable1.getSelectedRow()== -1){
                 JOptionPane.showMessageDialog(null, "Para modidficar debe seleccionar el idioma.");
               }
 else{
                     Integer fila = jTable1.getSelectedRow();
                     if(jTable1.getValueAt(fila, 0)==null || jTable1.getSelectedRowCount()>1)
                  JOptionPane.showMessageDialog(null, "Debe seleccionar un idioma.");
                     else{
                     if(jTextField1.getText().isEmpty() )
               JOptionPane.showMessageDialog(null, "No debe dejar campos vacios.");
                   else
           {
              IdiomaControl ic= new IdiomaControl();

                   if(ic.existe(jTextField1.getText())&& jTextField1.getText().equalsIgnoreCase(String.valueOf(jTable1.getValueAt(fila, 0)))==false){
                    JOptionPane.showMessageDialog(null, "Ya existe el idioma.");
                   }
 else{
                        try {
                            String escribe = String.valueOf(jComboBox4.getSelectedItem());
                            String lee = String.valueOf(jComboBox3.getSelectedItem());
                            String habla = String.valueOf(jComboBox2.getSelectedItem());
                            String dificultad = String.valueOf(jComboBox1.getSelectedItem());
                            int id = ic.buscarIdIdioma(String.valueOf(jTable1.getValueAt(fila, 0)));
                            ic.actualizarIdioma(jTextField1.getText(), dificultad, id, habla, escribe, lee);
                            Idioma idiom=ic.buscarIdiomaId(id);
                            idiomaList.remove(idiom);
                            idiomaList.add(idiom);
                             jTextField1.setText("");
           jComboBox3.setSelectedIndex(0);
         jComboBox4.setSelectedIndex(0);
          jComboBox1.setSelectedIndex(0);
           jComboBox2.setSelectedIndex(0);
                        } catch (PreexistingEntityException ex) {
                            Logger.getLogger(GestionarIdioma.class.getName()).log(Level.SEVERE, null, ex);
                        } catch (Exception ex) {
                            Logger.getLogger(GestionarIdioma.class.getName()).log(Level.SEVERE, null, ex);
                        }


 }
                         }
             }
        }
    }//GEN-LAST:event_jButton2MousePressed

