    private void jButton1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MousePressed
        // TODO add your handling code here:
             if (jTable1.getSelectedRow()!= -1){
             jTable1.clearSelection();
             JOptionPane.showMessageDialog(null, "Para insertar no debe tener ningún rol seleccionado.");
        jTextField1.setText("");

         }
        else{
             if(jTextField1.getText().isEmpty()){
              JOptionPane.showMessageDialog(null, "No debe dejar campos vacíos.");
             }
 else{
               RolControl rc=new RolControl();
                 if(rc.existe(jTextField1.getText())){
                      JOptionPane.showMessageDialog(null, "Este rol ya existe.");
                 jTextField1.setText("");
                 }
                 else {
                    try {
                        String nombre = jTextField1.getText();
                        Rol r = new Rol();
                        r.setNombreRol(nombre);
                        rc.adicionar(r);
                        jTextField1.setText("");
                        rolList.add(r);
                    } catch (PreexistingEntityException ex) {
                        Logger.getLogger(GestionarRol.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (Exception ex) {
                        Logger.getLogger(GestionarRol.class.getName()).log(Level.SEVERE, null, ex);
                    }


                 }
 }
        }
    }//GEN-LAST:event_jButton1MousePressed

    private void jButton2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MousePressed
        // TODO add your handling code here:
         if (jTable1.getSelectedRow()== -1){
                 JOptionPane.showMessageDialog(null, "Para modidficar debe seleccionar el rol.");
               }
 else{
                     Integer fila = jTable1.getSelectedRow();
                     if(jTable1.getValueAt(fila, 0)==null || jTable1.getSelectedRowCount()>1)
                  JOptionPane.showMessageDialog(null, "Debe seleccionar un rol.");
                     else{
                     if(jTextField1.getText().isEmpty() )
               JOptionPane.showMessageDialog(null, "No debe dejar campos vacios.");
                   else
           {
               RolControl rc=new RolControl();

                   if(rc.existe(jTextField1.getText())&& jTextField1.getText().equalsIgnoreCase(String.valueOf(jTable1.getValueAt(fila, 0)))==false){
                    JOptionPane.showMessageDialog(null, "Ya existe el rol.");
                   }
 else{
                        try {
                            int id = rc.buscarIdRol(String.valueOf(jTable1.getValueAt(fila, 0)));
                            rc.actualizarRol(jTextField1.getText(), id);
                            Rol r=rc.buscarRolId(id);
                            rolList.remove(r);
                            rolList.add(r);

                            jTextField1.setText("");
                        } catch (PreexistingEntityException ex) {
                            Logger.getLogger(GestionarRol.class.getName()).log(Level.SEVERE, null, ex);
                        } catch (Exception ex) {
                            Logger.getLogger(GestionarRol.class.getName()).log(Level.SEVERE, null, ex);
                        }


 }
                         }
             }
        }
    }//GEN-LAST:event_jButton2MousePressed

    private void jButton3MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MousePressed
        // TODO add your handling code here:
          if (jTable1.getSelectedRow()== -1){
                 JOptionPane.showMessageDialog(null, "Para eliminar debe seleccionar el rol.");
               }
 else{
              Integer fila = jTable1.getSelectedRow();
              if(jTable1.getValueAt(fila, 0)==null || jTable1.getSelectedRowCount()>1)
                  JOptionPane.showMessageDialog(null, "Debe seleccionar un Rol.");
              else{
              if (JOptionPane.showConfirmDialog(null, "Realmente desea eliminar?") == JOptionPane.YES_OPTION) {

                   // Integer fila = jTable1.getSelectedRow();
                RolControl rc=new RolControl();
                Rol r= new Rol();
                    String nombre = String.valueOf(jTable1.getValueAt(fila, 0));
                       r=rc.buscarRol(nombre);
                    int id= rc.buscarIdRol(nombre);
//                    jTextField2.setText(String.valueOf(id));
                UsuarioControl uc=new UsuarioControl();
                    if (uc.usuarioAsigando(id)) {

                        JOptionPane.showMessageDialog(null, "No se puede eliminar mientras esté asignado a un periodista.");
                    } else {
                        try {
                            rc.eliminar(id);
                            jTextField1.setText("");
                            rolList.remove(r);
                        } catch (NonexistentEntityException ex) {
                            Logger.getLogger(GestionarRol.class.getName()).log(Level.SEVERE, null, ex);
                        }


                    }
                  }
                  }

        }
    }//GEN-LAST:event_jButton3MousePressed

